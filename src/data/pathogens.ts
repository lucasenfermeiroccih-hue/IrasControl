/** Returns all pathogens from all categories (deduplicated, sorted) */
export function getAllPathogens(): string[] {
  const all = Object.values(pathogensByCategory).flat();
  return [...new Set(all)].sort((a, b) => a.localeCompare(b, "pt-BR"));
}
